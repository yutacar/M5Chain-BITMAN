#include "bitman/sprites.hpp"

#include <array>

namespace bitman {
namespace {

constexpr Frame8 make(std::array<std::uint8_t, 8> rows)
{
    return Frame8(rows);
}

constexpr std::array<Frame8, 19> kSprites = {
    make({0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}),  // Blank
    // The canonical standing figure follows the original BITMAN product:
    // 2x2 head, narrow torso, detached arms, long legs and two-pixel feet.
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x24, 0x66}),  // IdleA
    make({0x18, 0x18, 0x3C, 0x99, 0x5A, 0x24, 0x24, 0x66}),  // IdleB
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x28, 0x50, 0xD8}),  // StepLeftA
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x48, 0xCC}),  // StepLeftB
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x14, 0x0A, 0x1B}),  // StepRightA
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x12, 0x33}),  // StepRightB
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x24, 0x66}),  // CrouchA
    make({0x00, 0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x66}),  // CrouchB
    make({0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x24, 0x66, 0x00}),  // JumpA
    make({0x00, 0x18, 0x18, 0x3C, 0x5A, 0x5A, 0x66, 0x00}),  // JumpB
    make({0x18, 0x18, 0x3C, 0x99, 0x5A, 0x24, 0x24, 0x66}),  // ShakeA
    make({0x18, 0x18, 0x3C, 0x5A, 0x99, 0x24, 0x24, 0x66}),  // ShakeB
    make({0x66, 0x24, 0x24, 0x5A, 0x5A, 0x3C, 0x18, 0x18}),  // HeadstandA
    make({0x66, 0x24, 0x24, 0x5A, 0x99, 0x3C, 0x18, 0x18}),  // HeadstandB
    make({0x5A, 0x5A, 0x3C, 0x18, 0x18, 0x24, 0x24, 0x66}),  // Surprise
    make({0xFF, 0x81, 0x81, 0x81, 0x81, 0x81, 0x81, 0xFF}),  // BoxOuter
    make({0x00, 0x7E, 0x42, 0x42, 0x42, 0x42, 0x7E, 0x00}),  // BoxMiddle
    make({0x00, 0x00, 0x3C, 0x24, 0x24, 0x3C, 0x00, 0x00}),  // BoxInner
};

constexpr std::array<const char*, 19> kPoseNames = {
    "blank",      "idle-a",     "idle-b",      "step-left-a", "step-left-b",
    "step-right-a", "step-right-b", "crouch-a",   "crouch-b",    "jump-a",
    "jump-b",     "shake-a",    "shake-b",     "headstand-a", "headstand-b",
    "surprise",   "box-outer",  "box-middle",  "box-inner",
};

}  // namespace

const Frame8& sprite(Pose pose)
{
    return kSprites[static_cast<std::size_t>(pose)];
}

const char* poseName(Pose pose)
{
    return kPoseNames[static_cast<std::size_t>(pose)];
}

}  // namespace bitman
